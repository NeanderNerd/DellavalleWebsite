        <h1>Soil Sampling</h1>
        <h4>Equipment:</h4>
        <ul>
            <li>Soil probe or shovel; however, backhoe, soil auger or posthole digger may be used.</li>
            <li>Clean moisture-proof quart-size bags. Use paper bags only if soil is very dry.</li>
            <li>Clean plastic bucket to mix samples, optional.</li>
            <li>Samples collected for microbiology, nematode and pesticide residue analysis generally need
                refrigeration so have a cooler and coolant available.</li>
        </ul>
        <h4>Procedure:</h4>
        <ul>
            <li>Walk a zigzag course around or through the sampling area taking 20 to 30 cores, enough to make a
                quart of soil. Stay away from field edges.
                <ul>
                    <li>For furrow or field crops, take soil 12 inches below the surface or to the plowing depth.
                    </li>
                    <li>For tree and vine crops, soil samples should be taken one-foot increments to the depth of
                        rooting which may require three to five samples from the same area.</li>
                </ul>
            </li>
            <li>Collect one quart of soil and put in a clean quart-size bag.</li>
            <li>If collecting soils from multiple sites, composite by mixing in clean plastic bucket and taking one
                quart per sample.</li>
            <li>Clearly print on each bag using an indelible marker or ballpoint pen your name/company, site and
                sample description.
