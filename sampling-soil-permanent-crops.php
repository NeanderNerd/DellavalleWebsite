<h1>Soil: Permanent Crops</h1>
	<p>Soil samples should be collected at one-foot increments to the rooting depth for the mature crop.  One-foot increments are needed to evaluate what roots will be exposed to as they grow.  This may require three to five vertical (i.e. 1, 2, 3 feet) samples from the each site.</p>
	<p>Results will vary widely if soil amendment or fertilizer injection bands become part of the sample.  Irrigation pushes nutrients and salts to the edges of the wetted areas.  It is best to avoid these locations or take a high number of random samples.  Under certain situations it is advisable to take samples in these zones specifically to determine accumulated extremes.</p>
	<p>Nutrients are often better evaluated by petiole or leaf samples because the root-depth and position of residual fertilizer levels vary, changing the amount nutrients available to the plant.  Tissue sampling will determine what the plant is absorbing; see <a href="sampling_tissue.html" class="links">Plant Tissue</a>.</p>
