<h1>Sampling Techniques for Soil and Plant Tissue</h1>
<p>The nutritional status of your crop or field can be established by laboratory analysis. Proper sampling is essential for valid results. A sample should be randomly obtained composite that represents the area to be evaluated.</p>

	<h4>Determine Area to be Sampled:</h4>
	<p>For uniform fields (or for an average of a field), samples should be taken from the entire field.  Divide the area into 10 or 40 acre lots (or any appropriate size) based upon the following:</p>
	<ol>
		<li>Soil type, texture or color</li>
		<li>Topography, slope, changes in slope, cuts and fills</li>
		<li>Variation in crop history</li>
		<li>Plant variety, rootstock, age, harvest quality, irrigation source or leaf coloration</li>
		<li>Good areas vs. poor areas</li>
	</ol>
	<p>Non-uniform fields should be sampled by taking a composite sample from areas with the same characteristics.</p>
